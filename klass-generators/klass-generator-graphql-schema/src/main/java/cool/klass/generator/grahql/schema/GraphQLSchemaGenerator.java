package cool.klass.generator.grahql.schema;

import java.io.FileNotFoundException;
import java.io.FileOutputStream;
import java.io.PrintStream;
import java.nio.file.Path;
import java.util.Objects;

import javax.annotation.Nonnull;

import cool.klass.model.meta.domain.api.Classifier;
import cool.klass.model.meta.domain.api.DataType;
import cool.klass.model.meta.domain.api.DomainModel;
import cool.klass.model.meta.domain.api.PrimitiveType;
import cool.klass.model.meta.domain.api.TopLevelElement;
import cool.klass.model.meta.domain.api.property.DataTypeProperty;
import org.eclipse.collections.api.list.ImmutableList;

public class GraphQLSchemaGenerator
{
    @Nonnull
    private final DomainModel domainModel;
    @Nonnull
    private final String      rootPackageName;
    @Nonnull
    private final String      applicationName;

    public GraphQLSchemaGenerator(
            @Nonnull DomainModel domainModel,
            @Nonnull String rootPackageName,
            @Nonnull String applicationName)
    {
        this.domainModel     = Objects.requireNonNull(domainModel);
        this.rootPackageName = Objects.requireNonNull(rootPackageName);
        this.applicationName = Objects.requireNonNull(applicationName);
    }

    public void writeSchemaFiles(@Nonnull Path outputPath)
    {
        String orderBySourceCode = this.domainModel
                .getClassifiers()
                .collect(this::getOrderBySourceCode)
                .makeString("");

        String scalarsSourceCode = this.getScalarsSourceCode();

        String topLevelElementsCode = this.domainModel
                .getTopLevelElements()
                .collect(this::getSourceCode)
                .makeString("");

        //language=GraphQL
        String sourceCode = ""
                + "# Generated by cool.klass.generator.grahql.schema.GraphQLSchemaGenerator\n"
                + "\n"
                + "# Order Bys\n"
                + orderBySourceCode
                + "\n"
                + "# Scalars\n"
                + scalarsSourceCode
                + "\n"
                + "# Top Level Elements\n"
                + topLevelElementsCode;

        Path schemaOutputPath = this.getOutputPath(outputPath);
        this.printStringToFile(schemaOutputPath, sourceCode);
    }

    @Nonnull
    private Path getOutputPath(@Nonnull Path outputPath)
    {
        String packageRelativePath = this.rootPackageName.replaceAll("\\.", "/");
        Path outputDirectory = outputPath
                .resolve(packageRelativePath)
                .resolve("graphql")
                .resolve("schema");
        outputDirectory.toFile().mkdirs();
        String fileName = this.applicationName + ".graphqls";
        return outputDirectory.resolve(fileName);
    }

    private String getScalarsSourceCode()
    {
        return this.getTemporalScalarsSourceCode() + this.getPrimitiveScalarsSourceCode();
    }

    private String getTemporalScalarsSourceCode()
    {
        boolean isTemporal = this.domainModel
                .getClassifiers()
                .anySatisfy(Classifier::isTemporal);

        if (isTemporal)
        {
            return """
                    scalar Instant
                    scalar TemporalInstant
                    scalar TemporalRange
                    """;
        }

        return "";
    }

    private String getPrimitiveScalarsSourceCode()
    {
        ImmutableList<DataType> dataTypes = this.domainModel
                .getClassifiers()
                .asLazy()
                .flatCollect(Classifier::getDeclaredDataTypeProperties)
                .collect(DataTypeProperty::getType)
                .toList()
                .toImmutable();
        boolean containsLong      = dataTypes.contains(PrimitiveType.LONG);
        boolean containsLocalDate = dataTypes.contains(PrimitiveType.LOCAL_DATE);

        String longSourceCode      = containsLong ? "scalar Long\n" : "";
        String localDateSourceCode = containsLocalDate ? "scalar LocalDate\n" : "";
        return longSourceCode + localDateSourceCode;
    }

    private String getOrderBySourceCode(Classifier classifier)
    {
        //language=GraphQL
        return "input _" + classifier.getName() + "OrderBy {\n"
                + "    attribute: _" + classifier.getName() + "Finder\n"
                + "    direction: OrderByDirection\n"
                + "}\n\n";
    }

    private String getSourceCode(@Nonnull TopLevelElement topLevelElement)
    {
        var visitor = new GraphQLElementToSchemaSourceVisitor();
        topLevelElement.visit(visitor);
        return visitor.getSourceCode();
    }

    private void printStringToFile(@Nonnull Path path, String contents)
    {
        try (var printStream = new PrintStream(new FileOutputStream(path.toFile())))
        {
            printStream.print(contents);
        }
        catch (FileNotFoundException e)
        {
            throw new RuntimeException(e);
        }
    }
}
